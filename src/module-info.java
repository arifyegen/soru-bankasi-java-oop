package sorular;

public class sorular {
	private String soru;
	private String siklar;
	// constructor eklemdi
	public soru () {
		this.soru = "";
		this.siklar = "";
	}
	// fonksiyon içerisine aktarmak için   
	public soru (String soru, String siklar ) {
		this.soru = soru;
		this.siklar = siklar;
	}
	
}